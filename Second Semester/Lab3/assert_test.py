# Файл для перевірки assert з введенням числа через input

# Запитуємо користувача на введення числа
user_input = input("Введіть число: ")

# Перевірка, чи є введене значення числом
assert user_input.isdigit(), "Введене значення не є числом"

# Якщо assert не викликає помилки, виводимо число
print(f"Ви ввели число: {user_input}")
